public class Calculator{
    //add function to add together 2 integers
    int add(int int1, int int2) {
        return int1 + int2;
    }

    //sub function which subtracts 2 integers
    int sub(int int1, int int2){
        return int1 - int2;
    }

    //multiply function to multiply 2 doubles
    double multiply(double dub1, double dub2){
        return dub1 * dub2;
    }

    //divide function to divide 2 doubles
    double divide(double dub1, double dub2){
        return dub1 / dub2;
    }

}