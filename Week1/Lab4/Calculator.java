package LABS.Week1.Lab4;

public class Calculator {

    int add(int a, int b){
        return a+b;
    }

    int sub (int a, int b){
        return a-b;
    }

    double multiply(double a, double b){
        return a * b;
    }

    double divide(double a, double b){
        return a / b;
    }
}
