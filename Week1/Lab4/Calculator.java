class Calculator {
    
    public int add (int x1, int x2) {
        return x1 + x2;
    }

    public int sub (int x1, int x2) {
        return x1 - x2;
    }

    public double multiply (double x1, double x2) {
        return x1 * x2;
    }

    public double divide (double x1, double x2) {
        return x1 / x2;
    }
}
