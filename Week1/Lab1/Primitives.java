class Primitives {

  public static void main(String args[]) {

    // bool
    boolean boo = true;
    System.out.println("this is a bool " + boo);

    // byte
    byte maxByte = 124;
    System.out.println("this is a byte " + maxByte);

    // Short
    short minShort = 32767;
    System.out.println("this is a minShort " + minShort);

    // int
    int maxInt = 1234567;
    System.out.println("this is an int " + maxInt);

    // long
    long javaFlex = 123456789l;
    System.out.println("this is a long " + javaFlex);

    // float
    float pie = 3.14237f;
    System.out.println("this is a float " + pie);

    // double
    double doubleYum = 56.2312456;
    System.out.println("this is a double " + doubleYum);
  }
}